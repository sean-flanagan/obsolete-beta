const cloneLevel = (baseIndex, overrides = {}) => {
  const level = JSON.parse(JSON.stringify(BASE_LEVELS[baseIndex]));
  const merged = {
    ...level,
    ...overrides,
    world: overrides.world ?? level.world,
    start: overrides.start ?? level.start,
    checkpoint: overrides.checkpoint ?? level.checkpoint,
    exitZone: overrides.exitZone ?? level.exitZone,
    socket: overrides.socket === undefined ? level.socket : overrides.socket,
    battery: overrides.battery === undefined ? level.battery : overrides.battery,
    gateConsole: overrides.gateConsole === undefined ? level.gateConsole : overrides.gateConsole,
    console: overrides.console === undefined ? level.console : overrides.console,
    gates: overrides.gates === undefined ? level.gates : overrides.gates,
    solids: overrides.solids === undefined ? level.solids : overrides.solids,
    decor: overrides.decor === undefined ? level.decor : overrides.decor,
    npcs: overrides.npcs === undefined ? level.npcs : overrides.npcs,
    fragments: overrides.fragments === undefined ? level.fragments : overrides.fragments,
    items: overrides.items === undefined ? level.items : overrides.items,
    hazards: overrides.hazards === undefined ? level.hazards : overrides.hazards,
    conveyors: overrides.conveyors === undefined ? level.conveyors : overrides.conveyors,
    checkpoints: overrides.checkpoints === undefined ? level.checkpoints : overrides.checkpoints,
  };
  return merged;
};

